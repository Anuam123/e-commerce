        var header = document.getElementById('header');
        var lastScrollPosition = 0;
        var isSticky = false;

        window.addEventListener('scroll', function() {
            var currentScrollPosition = window.pageYOffset || document.documentElement.scrollTop;

            if (currentScrollPosition > lastScrollPosition && !isSticky) {
                // Scrolling down for the first time
                header.classList.add('sticky');
                isSticky = true;
            } else if (currentScrollPosition < lastScrollPosition && isSticky && currentScrollPosition <= 0) {
                // Scrolling up and reaching the initial position
                header.classList.remove('sticky');
                isSticky = false;
            }

            lastScrollPosition = currentScrollPosition;
        });
